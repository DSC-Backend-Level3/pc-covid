package dto;

import java.io.Serializable;

/**
 *
 * @author DELL
 */
public class ProvinceDTO implements Serializable {

    private int id; //Province's id
    private String name;// the name of the province

    public ProvinceDTO() {
    }

    public ProvinceDTO(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
